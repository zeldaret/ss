// These two constants must be declared in this exact order, and as
// `const volatile`, so that MWCC eagerly allocates them to .sdata2 at
// declaration (rather than lazily at first use). This produces the
// .sdata2 layout [24.0f, 10.0f, <magic double>] that matches the original.
// Reordering or removing `volatile` breaks the .sdata2 byte match.
static f32 const volatile sDefaultPitchIdxTenths = 24.0f;
static f32 const volatile sPitchIdxTenthsScale = 10.0f;

f32 pitchScaleForPitchIdx(s32 idx) {
    s32 i = idx + 240;
    if (i < 0) {
        i = 0;
    }
    if (i > 480) {
        i = 480;
    }
    return sPitchScaleTable[i];
}

f32 pitchScaleForPitchIdxTenths(f32 idx) {
    s16 i;
    f32 tmp = sPitchIdxTenthsScale * idx;
    OSf32tos16(&tmp, &i);
    i += 240;
    if (i < 0) {
        i = 0;
    }
    if (i > 480) {
        i = 480;
    }
    return sPitchScaleTable[i];
}

f32 pitchIdxTenthsForPitchScale(f32 scale) {
    f32 r = sDefaultPitchIdxTenths;
    for (s32 i = 0; i < 481; i++) {
        if (scale < sPitchScaleTable[i]) {
            r = (f32)((i - 240) / 10);
            break;
        }
    }
    return r;
}
